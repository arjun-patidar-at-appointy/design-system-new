package com.core.designsystem.tokens.components.button

/**
 * MUI-style button variants.
 * - TEXT: No border/background (e.g. Cancel, low emphasis)
 * - CONTAINED: Solid background (primary action)
 * - OUTLINED: Border only (secondary action)
 */
enum class ButtonVariant {
    TEXT,
    CONTAINED,
    OUTLINED
}
